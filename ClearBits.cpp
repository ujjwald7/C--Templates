int ClearLastBits(int n,int i)
{
    int mask=(-1<<i);
    return n&mask;
}

int ClearRangeItoJ(int n,int i,int j)
{
    int a = (-1<<(j+1));
    int b = (1<<i)-1;
    int mask=a|b;
    return n&mask;
}

